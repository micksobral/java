package javapoo;

public class calculadora {
	public double somar ( double v1, double v2) {
	return  v1 + v2;
}
	public double somar (double a, double b, double c) {
		return a+b+c;
	}
	public double mult (double v1, double v2) {
		return (v1 * v2);
	}
	public double div (double v1, double v2) {
	    return (v1)/v2;
		}
	}

	
